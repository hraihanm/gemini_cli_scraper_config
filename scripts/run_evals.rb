#!/usr/bin/env ruby
require 'json'
require 'fileutils'
require_relative 'parser_tester'

scraper_dir = ARGV[0]
unless scraper_dir && Dir.exist?(scraper_dir)
  puts "Usage: ruby run_evals.rb <scraper_dir>"
  exit 1
end

evals_dir = File.join(scraper_dir, 'evals')
unless Dir.exist?(evals_dir)
  puts "No evals directory found at: #{evals_dir}"
  exit 0
end

puts "=== Running Evaluations for #{File.basename(scraper_dir)} ==="
total_fixtures = 0
passed_fixtures = 0

Dir.glob(File.join(evals_dir, '*').gsub('\\', '/')).each do |fixture_dir|
  next unless File.directory?(fixture_dir)
  fixture_name = File.basename(fixture_dir)
  input_html = File.join(fixture_dir, 'input.html')
  expected_json_path = File.join(fixture_dir, 'expected.json')
  
  unless File.exist?(input_html) && File.exist?(expected_json_path)
    puts "⚠ Skipping #{fixture_name} (missing input.html or expected.json)"
    next
  end
  
  total_fixtures += 1
  expected = JSON.parse(File.read(expected_json_path))
  
  # Setup mock vars from expected output
  vars = {
    'category_name' => expected['category'],
    'category_id' => expected['category_id'],
    'subcategory_name' => expected['sub_category'],
    'breadcrumb' => expected['category'] ? "#{expected['category']} > #{expected['sub_category']}" : "",
    'rank' => expected['rank_in_listing'],
    'listing_position' => expected['rank_in_listing'],
    'page_number' => expected['page_number'] || 1
  }
  
  # Run parser using ParserTester class
  tester = ParserTester.new
  parser_path = File.join(scraper_dir, 'parsers/details.rb')
  
  begin
    tester.run(nil, parser_path, vars, scraper_dir, input_html, { quiet: true, page_type: 'details' })
    output = tester.instance_variable_get(:@outputs)&.first
    
    if output.nil?
      puts "✗ #{fixture_name}: Failed (No output generated by parser)"
      next
    end
    
    # Compare fields
    mismatches = []
    ignored_keys = %w(scraped_at_timestamp) # Timestamp will differ
    
    expected.each do |k, v|
      next if ignored_keys.include?(k.to_s)
      out_val = output[k.to_s] || output[k.to_sym]
      
      # Convert numbers to string or comparison formats
      expected_val = v
      if out_val.to_s != expected_val.to_s
        mismatches << "#{k}: expected #{expected_val.inspect}, got #{out_val.inspect}"
      end
    end
    
    score = ((expected.size - ignored_keys.size - mismatches.size).to_f / (expected.size - ignored_keys.size) * 100).round(2)
    if mismatches.empty?
      puts "✓ #{fixture_name}: Passed (100% match)"
      passed_fixtures += 1
    else
      puts "✗ #{fixture_name}: Failed (#{score}% match)"
      mismatches.each { |m| puts "  - #{m}" }
    end
    
  rescue => e
    puts "✗ #{fixture_name}: Error during execution - #{e.message}"
    puts e.backtrace.first(3).join("\n")
  end
end

puts "\n=== Summary ==="
puts "Passed: #{passed_fixtures} / #{total_fixtures} fixtures"
if total_fixtures > 0 && passed_fixtures == total_fixtures
  puts "🎉 All evaluations passed successfully!"
  exit 0
else
  puts "✗ Some evaluations failed."
  exit 1
end
